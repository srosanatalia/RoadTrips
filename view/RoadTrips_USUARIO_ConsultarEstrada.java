    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        Iterator itr = facade.iteratorTodasArestas();
        while(itr.hasNext()){
            Object o = itr.next();
            if(o  instanceof Estrada){
                Estrada estrada = (Estrada)o;
                if(jComboBoxEstrada.getSelectedItem().equals(estrada.getNome())){
                    String nomeA, nomeB;
                    if(estrada.getSource() instanceof Cidade){
                        nomeA = ((Cidade)estrada.getSource()).getNome();
                    }
                    else{
                        nomeA = ((Interseccao)estrada.getSource()).getNome();
                    }
                    
                    if(estrada.getDestiny() instanceof Cidade){
                        nomeB = ((Cidade)estrada.getDestiny()).getNome();
                    }
                    else{
                        nomeB = ((Interseccao)estrada.getDestiny()).getNome();
                    }
                    
                    JOptionPane.showMessageDialog(rootPane, "Nome: "+estrada.getNome()+"\n"
                                                           +"Ponto Inicial: "+nomeA+"\n"
                                                           +"Ponto Final: "+nomeB+"\n"
                                                           +"Distância: "+estrada.getWeight()+"\n"
                    );
                }
            }
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed


