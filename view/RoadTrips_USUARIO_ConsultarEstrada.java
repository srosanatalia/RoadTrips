    public void preencheEstradas(){
        jComboBoxEstrada.removeAllItems();
        jComboBoxEstrada.addItem("<Selecionar Opção>");
        
        Iterator itr = facade.iteratorTodasArestas();
        while(itr.hasNext()){
            Object o = itr.next();
            if(o  instanceof Estrada){
                Estrada estrada = (Estrada)o;
                jComboBoxEstrada.addItem(estrada.getNome());
            }
        }
    }
    
    
