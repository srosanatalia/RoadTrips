    public void preencheCidades(){
        jComboBoxCidadeInicial.removeAllItems();
        jComboBoxCidadeInicial.addItem("<Selecionar Opção>");
        jComboBoxCidadeFinal.removeAllItems();
        jComboBoxCidadeFinal.addItem("<Selecionar Opção>");
        
        Iterator itr = facade.iteratorTodasCidade();
        while(itr.hasNext()){
            Object o = itr.next();
            if(o  instanceof Cidade){
                Cidade cidade = (Cidade)o;
                jComboBoxCidadeInicial.addItem(cidade.getNome());
                jComboBoxCidadeFinal.addItem(cidade.getNome());
            }
            else{
                Interseccao interseccao = (Interseccao)o;
                jComboBoxCidadeInicial.addItem(interseccao.getNome());
                jComboBoxCidadeFinal.addItem(interseccao.getNome());
            }
        }
    }
    
    
