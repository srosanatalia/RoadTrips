    private void jButtonGerarViagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGerarViagemActionPerformed
        Ponto a = null, b = null;
        
        Iterator itr = facade.iteratorTodasCidade();
        while(itr.hasNext()){
            Object o = itr.next();
            if(o  instanceof Cidade){
                Cidade cidade = (Cidade)o;
                if(jComboBoxCidadeInicial.getSelectedItem().equals(cidade.getNome())){
                    a = cidade;
                }
                if(jComboBoxCidadeFinal.getSelectedItem().equals(cidade.getNome())){
                    b = cidade;
                }
            }
            else{
                Interseccao interseccao = (Interseccao)o;
                if(jComboBoxCidadeInicial.getSelectedItem().equals(interseccao.getNome())){
                    a = interseccao;
                }
                if(jComboBoxCidadeFinal.getSelectedItem().equals(interseccao.getNome())){
                    b = interseccao;
                }
            }
        }
        
    }//GEN-LAST:event_jButtonGerarViagemActionPerformed


